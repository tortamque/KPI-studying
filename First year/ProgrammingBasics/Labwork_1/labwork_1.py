a = float(input("a = "))  # введення a (a - перший член арифметичної прогресії)
d = float(input("d = "))  # введення d (d - різниця прогресії)
n = int(input("n = "))  # введення n (n - кількість членів прогресії)
sum = (2 * a + d * (n - 1)) / 2 * n  # обчислення суми
print(f"The result is {sum}")  # виведення результату